        for layer in self.allLayersLevel:

            y = 0
            for array in layer:
                x = 0
                for num in array:

                    if num == 1:
                        grass = Grass('baixa', x * TILESIZE, y * TILESIZE, TILESIZE, TILESIZE, (0, 100, 0))
                        self.main.screen.blit(grass.image, (grass.rect.x, grass.rect.y))
